export const getCourseDefinition = async (gameDefinition:any, fetch?:any, wip?:boolean) => {
    return new Promise( (resolve, reject)=>{
        const modeCollection = courseDefinitionsRepo[gameDefinition.type];
        const localCourse = modeCollection && courseDefinitionsRepo[gameDefinition.type][gameDefinition.subType||"1"][gameDefinition.courseId];
        if(localCourse){
            setTimeout(()=>{
                resolve(localCourse)
            },400);
        }else{
            const baseURL = USE_REMOTE_SERVER?`https://golfcraftgame.com`:`http://localhost:2569`;
            fetch(`${baseURL}/api/get-course${wip?"-wip":""}`, {
                body:JSON.stringify({
                    alias:gameDefinition.courseId
                }),
                method:"POST",
                headers:{
                    "Content-Type":"application/json"
                }
            }).then(async(r)=>{
                try{
                    return await r.json()
                }catch(error){
                    return reject({error, status:r.status});
                }
            }).then((data)=>{
                if(data?.error || !data){
                    return reject(data?.error);
                }
                    resolve({
                        ...JSON.parse(data.definition),//TODO it should come already parsed from server
                        metadata:JSON.parse(data.metadata),
                        createdBy:data.createdBy,
                        timesPlayed:data.timesPlayed,
                        timesAbandoned:data.timesAbandoned,
                        averageTime:data.averageTime,
                        evaluation:data.evaluation||65,
                        authorName:data.authorName,
                        averageArea:data.averageArea,
                        averageDistance:data.averageDistance,
                        averageShoots:data.averageShoots,
                        displayName:data.displayName || data.alias,
                        ID:data.ID,
                        alias:data.alias,
                        collectionId:data.collectionId,
                        isSeason:data.isSeason
                    });
            }, ()=>{

            });
        }

    })
}